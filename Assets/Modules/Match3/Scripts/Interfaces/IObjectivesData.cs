namespace Modules.Match3.Scripts.Interfaces
{
    /// <summary>
    /// Интерфейс для доступа к данным целей (объективов) в Match3 игре.
    /// Предоставляет информацию о параметрах игровых целей, таких как количество доступных ходов.
    /// </summary>
    public interface IObjectivesData
    {
        /// <summary>
        /// Получает количество доступных ходов для текущего раунда.
        /// </summary>
        /// <returns>
        /// Количество оставшихся ходов. Используется для отслеживания прогресса выполнения целей раунда.
        /// </returns>
        int GetTurnsCount();
    }
}
