using Modules.ECS.Scripts.Match3.Components;
using System.Collections.Generic;

namespace Modules.Match3.Scripts.Interfaces
{
    /// <summary>
    /// Интерфейс для доступа к данным целей (объективов) в Match3 игре.
    /// Предоставляет информацию о параметрах игровых целей, таких как количество доступных ходов
    /// и начальные значения очков различных типов счетчиков (буст, очки здоровья, щиты, прогресс и т.д.).
    /// </summary>
    public interface IObjectivesData
    {
        /// <summary>
        /// Получает количество доступных ходов для текущего раунда.
        /// </summary>
        /// <returns>
        /// Количество оставшихся ходов. Используется для отслеживания прогресса выполнения целей раунда.
        /// </returns>
        int GetTurnsCount();

        /// <summary>
        /// Получает список начальных значений очков для различных типов счетчиков.
        /// Используется для инициализации системы учета очков в начале раунда.
        /// </summary>
        /// <returns>
        /// Список данных о начальных значениях очков. Каждый элемент содержит тип счетчика (ScoreType)
        /// и его начальное значение. Используется для установки начального состояния счетчиков очков,
        /// буста, здоровья, щитов, прогресса и других игровых метрик.
        /// </returns>
        List<ScoreData> GetStartScoreValues();
    }
}
