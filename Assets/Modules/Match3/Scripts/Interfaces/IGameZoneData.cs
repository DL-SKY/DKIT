namespace Modules.Match3.Scripts.Interfaces
{
    /// <summary>
    /// Интерфейс для доступа к данным игровой зоны в Match3 игре.
    /// Предоставляет информацию о структуре игрового поля, включая маску доступных ячеек
    /// и пресеты для начальной инициализации гемов.
    /// </summary>
    public interface IGameZoneData
    {
        /// <summary>
        /// Получает маску игровой зоны, определяющую доступные ячейки на игровом поле.
        /// </summary>
        /// <returns>
        /// Двумерный массив целых чисел, где каждое значение определяет тип ячейки.
        /// Значения сопоставляются с определениями ячеек через CellsMap для определения валидности ячейки.
        /// </returns>
        int[,] GetMask();

        /// <summary>
        /// Получает пресеты для начальной инициализации гемов на игровом поле.
        /// </summary>
        /// <returns>
        /// Двумерный массив целых чисел, содержащий начальные значения/типы гемов для каждой ячейки.
        /// Размерность массива должна соответствовать размерности маски игровой зоны.
        /// </returns>
        int[,] GetPresets();
    }
}
